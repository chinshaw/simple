package com.simple.radapter.api;

public class RAdapterException extends Exception {

	/**
	 * Serialization Id
	 */
	private static final long serialVersionUID = -8089593376496864714L;

	public RAdapterException() {
		super();
	}

	public RAdapterException(String message, Throwable cause) {
		super(message, cause);
	}

	public RAdapterException(String message) {
		super(message);
	}

	public RAdapterException(Throwable cause) {
		super(cause);
	}

}
