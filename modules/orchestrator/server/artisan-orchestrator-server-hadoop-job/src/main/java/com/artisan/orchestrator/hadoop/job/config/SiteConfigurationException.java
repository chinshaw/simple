package com.artisan.orchestrator.hadoop.job.config;

public class SiteConfigurationException extends Exception {

	public SiteConfigurationException() {
		
	}

	public SiteConfigurationException(String message) {
		super(message);
		
	}

	public SiteConfigurationException(Throwable cause) {
		super(cause);
		
	}

	public SiteConfigurationException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public SiteConfigurationException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}
}
